""" 1861. 旋转盒子
给你一个 m x n 的字符矩阵 boxGrid ，它表示一个箱子的侧视图。
箱子的每一个格子可能为： '#' 表示石头  '*' 表示固定的障碍物  '.' 表示空位置
这个箱子被 顺时针旋转 90 度 ，由于重力原因，部分石头的位置会发生改变。每个石头会垂直掉落，直到它遇到障碍物，另一个石头或者箱子的底部。
重力 不会 影响障碍物的位置，同时箱子旋转不会产生惯性 ，也就是说石头的水平位置不会发生改变。
题目保证初始时 boxGrid 中的石头要么在一个障碍物上，要么在另一个石头上，要么在箱子的底部。
请你返回一个 n x m 的矩阵，表示按照上述旋转后，箱子内的结果。
"""

""" 【翻译】
有一个神秘的箱子，里面住着三种居民：坚硬的石头（#）、顽固的障碍物（*），以及空荡荡的空气（.）。
你决定把这个箱子顺时针旋转 90 度，然后让重力发挥作用——石头们会垂直下落，直到碰到障碍物、另一块石头，或者箱子的底部。
箱子旋转不会产生惯性，石头的水平位置（旋转后对应新方向）不会改变。请你预测最终箱子的模样。

请闭上眼睛，想象这个旋转过程：
原来的顶部变成了右边，原来的底部变成了左边，原来的一行变成了一列。
现在，重力方向指向新箱子的底部（也就是原来箱子的左侧）。石头们开始下落，但它们不能穿过障碍物，也不能和另一块石头重叠在一个格子内。

你灵机一动：先旋转，再让石头下落。这样问题就变成了：
旋转后的矩阵里，每一列从上往下看，石头会掉到该列的底部，遇到障碍物就停在上方。

于是你拿出魔法笔，开始操作。
🔸旋转：
    把原来的 m x n 矩阵变成 n x m 的临时矩阵。
    原来 (i, j) 位置的元素，旋转后到了 (j, m - 1 - i) —— 因为顺时针旋转 90 度，原来的第 0 行变成了最后一列。
🔸下落：
    对旋转后的每一列，从底部向上看。你用一个指针 bottom 指向当前列最下面可以放石头的位置（初始为最后一行）。
    然后从该列的最后一行向上遍历：
        如果遇到障碍物 *，就把 bottom 移到障碍物的上一行（因为障碍物下面不能放石头）。        
        如果遇到石头 #，就把这块石头搬到 bottom 位置，然后把 bottom 上移一行，同时把原来的位置设为空气（.）。        
        遇到空气就跳过。
    这样，每一列处理完毕，石头就全部落到了该落的位置。

你看着这个过程，就像在看一瓶混有弹珠和木块的透明水瓶：把瓶子旋转 90 度，弹珠会齐刷刷地掉向新的底部，而木块纹丝不动。
每一列的下落就像一个独立的“弹珠滑梯”——从下往上数，遇到木块就重新定义地面，遇到弹珠就让它掉到当前地面的最底部。

最后，你得到了旋转并重力作用后的新矩阵。
你忽然觉得：这不仅是石头的冒险，也是生活中“换个角度，再让一切自然沉淀”的智慧。
有时候，先旋转视角，再让重力（规律）发挥作用，就能让混乱变清晰。

"""
from typing import List


class Solution:
    def rotateTheBox(self, boxGrid: List[List[str]]) -> List[List[str]]:

        # 第一步：顺时针旋转 90 度（原矩阵 m 行 n 列 -> 新矩阵 n 行 m 列）
        # 使用 zip(*reversed(boxGrid)) 得到旋转后的矩阵
        rotated = [list(row) for row in zip(*reversed(boxGrid))]

        n = len(rotated)  # 旋转后的行数
        m = len(rotated[0])  # 旋转后的列数

        # 第二步：对每一列模拟重力（石头垂直下落）
        for c in range(m):
            write = n - 1  # 指向当前列可放置石头的底部行（从下往上）
            # 从底部向上扫描
            for r in range(n - 1, -1, -1):
                if rotated[r][c] == '*':
                    # 遇到障碍物，石头只能落在障碍物的上方
                    write = r - 1
                elif rotated[r][c] == '#':
                    # 遇到石头，将其移动到 write 指针位置
                    if r != write:
                        rotated[write][c] = '#'
                        rotated[r][c] = '.'
                    write -= 1
                # 遇到 '.' 什么也不做，继续向上扫描

        return rotated


if __name__ == "__main__":
    s = Solution()
    print(s.rotateTheBox([["#", ".", "*", "."], ["#", "#", "*", "."]]))


