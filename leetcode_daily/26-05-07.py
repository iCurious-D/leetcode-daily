""" 3660. 跳跃游戏 IX
给你一个整数数组 nums。
从任意下标 i 出发，你可以根据以下规则跳跃到另一个下标 j：
仅当 nums[j] < nums[i] 时，才允许跳跃到下标 j，其中 j > i。
仅当 nums[j] > nums[i] 时，才允许跳跃到下标 j，其中 j < i。
对于每个下标 i，找出从 i 出发且可以跳跃 任意 次，能够到达 nums 中的 最大值 是多少。
返回一个数组 ans，其中 ans[i] 是从下标 i 出发可以到达的最大值。
"""
""" 【翻译】
江湖上有一条绵延的山脉，从左到右排开，每个山头都有一个高度 nums[i]。
你站在某一个山头上，练成了两招离谱的轻功：
- **向右纵身**：可以瞬间飞到右边 **任意一个比你当前山头更矮** 的山头（`j > i` 且 `nums[j] < nums[i]`）。
- **向左纵身**：可以瞬间飞到左边 **任意一个比你当前山头更高** 的山头（`j < i` 且 `nums[j] > nums[i]`）。

你可以不限次数地使用这两招，飞来飞去。
现在问你：从每一个山头出发，你这辈子能到达的 **最高山头** 是多高？
---

### 两股神秘力量

故事要从右往左讲。  
我们先派出两个“巡山使”，一个从左往右走，一个从右往左走。

- **左巡山使（记录前缀最大值）**：他每经过一个山头，就记下从最左边到这里为止，见过的最高山头的高度。  
  我们称这个高度为 **护法**——在当前位置及左边，能罩着你的最大靠山。  
  数组：`backward_larger[i]` = 从 `0` 到 `i` 的最大值。

- **右巡山使（记录后缀最小值）**：他从右往左走，每经过一个山头，就记下从这里到最右边为止，见过的最低山头的高度。  
  我们称这个高度为 **门槛**——你如果想从左边跳到右边去，必须至少有一个山头的高度超过这个门槛，才能形成“左高右低”的跳板。  
  数组：`forward_smaller[i]` = 从 `i` 到 `n-1` 的最小值。

---

### 从最右端开始破局

最右侧的山头 `n-1`，它右边没有路了，它能够到达的最高山头，其实就是整条山脉里的最高峰（因为从它出发可以一路向左，往高处跳，最终必能抵达最高峰）。  
所以 `ans[n-1]` = 整条山脉的最高值 = `backward_larger[n-1]`。

---

### 一路向左，传檄而定

现在，我们站在山头 `i`（从右往左走），手里已经握着右边山头 `i+1` 的答案 `ans[i+1]`。

眼前摆着两个关键数字：

- `backward_larger[i]`：你和你左边的 **最强护法**。
- `forward_smaller[i+1]`：紧挨着你右边的整个右半区的 **最低门槛**。

**判断：**

如果 **最强护法 > 最低门槛**，这说明在你左边的区域里，存在一个高度大于右边某个矮山头的“高人”。  
这个高人可以先向右跳到那个矮山头（符合向右跳的规矩：目标更矮），然后那个矮山头再施展轻功，就能一路通向右边世界里的最高峰。  
因此，只要你能够得着这位护法（通过向左跳往高处，或者你本身就是这位护法），你就能借着他的力量 **突破门槛**，最终抵达右半区能够抵达的最高峰。  
反应在数字上就是：`ans[i] = ans[i+1]`。  
——右边兄弟能到多高，你就能到多高，因为护法给你开了一条暗度陈仓的路。

如果 **最强护法 ≤ 最低门槛**，那可就哀了。  
你和左边所有山头，没有任何一个能高过右边那座最矮的门槛。你想向右跳，目标就必须比你矮，可右边连最矮的都跟你一样高（或更高），你这一跳根本落不了地，直接摔死。  
向左跳也只能在左边这一亩三分地里转悠。  
于是你这辈子的上限，就是你和你左边这些山头里的最高者——也就是 **护法本身**。  
所以 `ans[i] = backward_larger[i]`。

---

### 收刀入鞘

就这样，从最右端开始，一路向左，每到一座山头，就看看护法是否能劈开门槛。  
劈得开，则继承右边的答案；劈不开，则固守自己的护法。  
等走到最左端，所有人的答案都已了然。

这比盟主一封封发英雄帖要轻巧得多：你只需要两个巡山使走过一遍山脉，再让一个账房先生从右往左拨一遍算盘，答案就跃然纸上。

---

**复杂度**  
- 时间：`O(n)`，只扫描三遍数组。  
- 空间：`O(n)`，存储前缀最大值、后缀最小值和答案。

你只需要记得，每个山头能走多远，不取决于它自己的武功，而取决于它所在的那半边江湖里，最高的那位护法能不能劈开另外半边的门槛。  
能，则天下大可去得；不能，则一亩三分地，护法就是天。

"""
from typing import List


class Solution:
    def maxValue(self, nums: List[int]) -> List[int]:
        n = len(nums)

        # 左巡山使：记录从0到i的最大值（护法）
        backward_larger = [0] * n
        backward_larger[0] = nums[0]
        for i in range(1, n):
            backward_larger[i] = max(nums[i], backward_larger[i - 1])

        # 右巡山使：记录从i到n-1的最小值（门槛）
        forward_smaller = [0] * n
        forward_smaller[n - 1] = nums[n - 1]
        for i in range(n - 2, -1, -1):
            forward_smaller[i] = min(nums[i], forward_smaller[i + 1])

        # 从右向左推算答案
        ans = [0] * n
        ans[n - 1] = backward_larger[n - 1]  # 最右端：全局最高
        for i in range(n - 2, -1, -1):
            if backward_larger[i] > forward_smaller[i + 1]:
                # 护法劈开门槛，继承右边的最高
                ans[i] = ans[i + 1]
            else:
                # 困守左边，最高就是护法本身
                ans[i] = backward_larger[i]

        return ans


if __name__ == '__main__':
    s = Solution()
    print(s.maxValue([2, 3, 1]))
    print(s.maxValue([2, 1, 3]))
    print(s.maxValue([30, 21, 5, 35, 24]))


