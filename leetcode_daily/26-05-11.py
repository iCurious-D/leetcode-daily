""" 2553. 分割数组中数字的数位
给你一个正整数数组 nums ，请你返回一个数组 answer ，你需要将 nums 中每个整数进行数位分割后，按照 nums 中出现的 相同顺序 放入答案数组中。
对一个整数进行数位分割，指的是将整数各个数位按原本出现的顺序排列成数组。
比方说，整数 10921 ，分割它的各个数位得到 [1,0,9,2,1] 。

------《数位分割大法》--------------------------------------------------------------------
话说江湖之上，有一道名为 "2553. 分割数组中数字的数位" 的武学秘籍。
此秘籍记载了一门奇功：如何将一个完整的整数，拆解成一个个独立的数位，如同将一套完整的拳法拆成一招一式。

---------------------------------------------------------------------------------------
第一式：字符串化骨绵掌（推荐）

心法口诀 > "数字本无形，化作字符串。一字一字符，逐个取出来。"

此功法看似轻柔，实则威力无穷。将 `10921` 化作 `"10921"`，如同将一块坚冰化成流水，再一滴一滴收取。
`'1'`、`'0'`、`'9'`、`'2'`、`'1'`，五个字符，依次取出，转回整数，便是 `[1, 0, 9, 2, 1]`。

优点：简单直接，老少咸宜，不易出错。
缺点：需要额外的字符串转换，内力消耗稍大。

---
第二式：取余反转大法（数学正统）

心法口诀 > "取余得末位，整除去尾端。逆序方为正，反转见真章。"

此功法分为四个步骤：
    第一招：`tmp = []` —— 空杯待物: 创建一个临时容器 `tmp`，如同准备一个空碗，用来盛放即将取出的数位。
    第二招：`while num:` —— 循环不止: 只要 `num` 不为零，就继续施展功法。如同内力运转，周而复始。
    第三招：`tmp.append(num % 10)` —— 取余点穴: 这是整套功法的**核心精髓**！
        `%` 取余运算符，如同点穴手，精准地点中数字的**最低位**。
        每一次，都只取 **0-9** 之间的单个数字，这才是真正的"数位"！
    第四招：`num = num // 10` —— 整除截尾: 取完最低位后，用 `//` 整除去掉它。
    第五招：`tmp.reverse()` —— 乾坤倒转: 上述取法是从低位到高位，得到的 `tmp`，与原数的顺序相反！因此需要 `reverse()` 反转，方为正解。
    第六招：`ans.extend(tmp)` —— 万流归宗：将当前数字的数位数组合并到总答案中，继续处理下一个数字。


----------------------------------------------------------------------
两大神功对比
 ---------------------------------------------------------------------
|      功法     |       优点         |      缺点       |   适用人群      |
|--------------|-------------------|----------------|----------------|
| 字符串化骨绵掌 | 代码简洁，不易出错    | 需类型转换，稍慢 | 初学者、追求效率者 |
| 取余反转大法   | 纯数学运算，内力深厚  | 需反转，步骤较多 | 数学派、面试装逼者 |
---------------------------------------------------------------------

## 终章：结语
欲练此功，必先理解：
- **`%` 取余**：获取最低位的数字（真正的数位）
- **`//` 整除**：去掉最低位，缩小数字
- **`reverse()`**：反转顺序，恢复原貌

练就此功，江湖再无难拆之数。

"""
from typing import List


class Solution:
    def separateDigits_1(self, nums: List[int]) -> List[int]:
        ans = []
        for num in nums:
            for ch in str(num):  # 化骨绵掌：将数字化为字符串
                ans.append(int(ch))  # 逐个字符取出，还原为整数
        return ans

    def separateDigits_2(self, nums: List[int]) -> List[int]:
        ans = []
        for num in nums:
            tmp = []  # 临时容器，存放数位
            while num:  # 当数字不为零时循环
                tmp.append(num % 10)  # 取余数：获取最低位的数字
                num = num // 10  # 整除：去掉最低位
            tmp.reverse()  # 反转：恢复从高到低的顺序
            ans.extend(tmp)  # 合并到答案数组
        return ans


if __name__ == '__main__':
    print(Solution().separateDigits_1([13, 25, 37, 49]))
    print(Solution().separateDigits_2([13, 25, 37, 49]))
