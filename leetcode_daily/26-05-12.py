from typing import List


class Solution:
    def minimumEffort(self, tasks: List[List[int]]) -> int:
        # tasks.sort(key=lambda x: x[1] - x[0])
        # ans = 0
        # for task in tasks:
        #     ans = max(ans+task[0], task[1])
        # return ans

        # 按恶心指数降序排
        tasks.sort(key=lambda x: x[1] - x[0], reverse=True)

        cur_health = 0
        total_bottle = 0  # 总共氪了多少瓶血

        for actual, minimum in tasks:
            if cur_health < minimum:
                # 氪金补血
                total_bottle += minimum - cur_health
                cur_health = minimum
            # 打怪掉血
            cur_health -= actual

        return total_bottle


