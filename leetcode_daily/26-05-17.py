"""  154. 寻找旋转排序数组中的最小值 II
已知一个长度为 n 的数组，预先按照升序排列，经由 1 到 n 次 旋转 后，得到输入数组。
例如，原数组 nums = [0,1,2,4,5,6,7] 在变化后可能得到：若旋转 4 次，则可以得到 [4,5,6,7,0,1,2], 若旋转 7 次，则可以得到 [0,1,2,4,5,6,7]
注意，数组 [a[0], a[1], a[2], ..., a[n-1]] 旋转一次 的结果为数组 [a[n-1], a[0], a[1], a[2], ..., a[n-2]] 。

给你一个可能存在 重复 元素值的数组 nums ，它原来是一个升序排列的数组，并按上述情形进行了多次旋转。请你找出并返回数组中的 最小元素 。
你必须尽可能减少整个过程的操作步骤。

==============================================================================

**迷雾中的红蓝界——当数组学会了分身**

上回书说到，你从破酒馆出来，已然参透了**“红蓝染色·开区间二分剑”**的奥义。那
时的你，在旋转数组的江湖中游刃有余，红蓝染料泼得那叫一个潇洒，仿佛世间万物非黑即白，非红即蓝。

然而，江湖险恶，人心难测。就在你以为可以收山隐退、去海边开个”二分凉面馆“的时候，那个叫“旋转”的魔头又杀回来了。
而这次，他不仅卷土重来，还学会了一种更阴间的法术——**“分身术”**（重复元素）。

以前的数组：`[4, 5, 6, 7, 0, 1, 2]`，每个数字棱角分明，姓名牌别在胸口。
现在的数组：`[2, 2, 2, 0, 1, 2, 2]`，甚至还有丧心病狂的 `[3, 3, 1, 3, 3, 3]`。

看着满屏克隆人，你惊呼：“这怎么染？`mid` 是 2，最后一个也是 2，我该把它泼成红色还是蓝色？红蓝染色都快成色盲测试了！”
正当你对着克隆人军团头皮发麻时，背后传来一个懒洋洋的声音。

“啧啧，开区间剑法练得不错，可惜只学了上半部。”
你猛然回头——还是那个披着斗篷的仙人。只见他把斗篷裹紧了些，像看一只踩进陷阱的野兔一样看着你。

“欢迎来到**重复元素地狱**。在这里，逻辑会模糊，边界会消失，连红蓝颜料都会被搅成灰紫色漩涡。
但记住，盟主的命令没变——**尽可能 O(log n)**。虽然在这鬼地方最坏情况会退化到 O(n)，但我们还是要用最短的步数逼近真相。”

------

##### 第一幕：当红蓝染色遇上双面间谍

你慌忙掏出上一部的心法，剑指一掐：

```python
if nums[mid] < nums[-1]:
    right = mid
else:
    left = mid
```

在 `[2, 2, 2, 0, 1, 2, 2]` 面前，剑尖刚碰到数组就折了。
你选中索引 2 的 `2`，最后一个元素也是 `2`。`2 < 2`？不成立。`2 > 2`？也不成立。它们勾肩搭背，向你露出了一个无辜的微笑。

此时的 `mid` 就像一个双面间谍——它可能是红区卧底（右边的 2），也可能只是蓝区一个换错衣服的伪装者（左边的 2）。你根本不知道最小值躲在它左边还是右边。
更操蛋的是，上一章你依赖的定海神针 `nums[-1]` 也叛变了。
在重复元素的世界里，最后一个元素很可能和周围一个模子刻出来的，它不再能当分水岭。
你试图换成 `nums[right]`，可如果 `right` 纹丝不动，照样是同一个数字来回比对，局面毫无进展。

------

##### 第二幕：混沌右缩——第三条路

“在元素互不相同的数组里，只有大于和小于两种情况。大于染红，小于染蓝，干净利落。
但重复元素一来，**等于**的情况就冒出来了。比如 `nums[mid] == nums[-1]`——你不知道它是在红区，还是在蓝区，还甚至在两区交界处。”
仙人把手指插进红蓝颜料交汇处，轻轻一搅——红与蓝混在一起，变成了混沌的紫色漩涡。

“面对重复元素的迷魂大法，你不能固守‘大于小于’的老规矩，而必须悟出第三条路。”

你追问：“那第三条路是什么？”

仙人抄起酒壶，一饮而尽，从袖子里抽出一柄闪着寒光的小刀：

```python
left, right = -1, len(nums) - 1
while left + 1 < right:
    mid = left + (right - left) // 2
    if nums[mid] < nums[right]:
        right = mid      # 蓝染右收
    elif nums[mid] > nums[right]:
        left = mid       # 赤染左推
    else:  # nums[mid] == nums[right]
        right -= 1       # 混沌右缩
return nums[right]
```

你死死盯住新添的那一行：**right -= 1**。
“这是什么操作？之前的优雅对称呢？之前的红蓝艺术呢？这一个 `-= 1`，简直像拿擀面杖敲蒙娜丽莎！”

仙人微微一笑。
“小子，在重复元素的地狱里，固定锚点已经失效，我们必须让参照物也动起来。”

“所以，我们现在用的参照物不再是 `nums[-1]`，而是 **nums[right]**。
当 `mid` 和 `right` 指向同一个数，我们唯一能确定的，就是 `right` 这个位置肯定不是唯一的最小值——至少 `mid` 那里还有一个备份。”

“ `right -= 1` 不是斩杀，而是剔除，是**把右边界往左缩一位，看看下一个会不会露出马脚**。
它缩得很慢，但绝不误杀。因为只要 `mid` 还在，同样的数值就不会丢失。”

仙人又蘸着酒水，把三种剑路画在桌上：

1. **nums[mid] < nums[right] → 蓝染右收**
   `mid` 比参照物小，铁定掉进蓝区。最小值在 `mid` 或左边。右边界缩到 `mid`，保留希望。
2. **nums[mid] > nums[right] → 赤染左推**
   `mid` 比参照物大，高居红区。最小值在右边。左边界推进到 `mid`，红区扩张。
3. **nums[mid] == nums[right] → 混沌右缩**
   分不清？那就踢掉一个。换一个参照物，重新来过。

你反复咀嚼，恍然大悟，却又立刻皱起眉头：
“但如果是 `[1,1,1,1,1]` 这种全员克隆人军团，岂不是要一次次 `right -= 1`，从 O(log n) 一路退化到 O(n)？”

仙人点头，眼中闪过一丝无奈：“没错。**重复元素就是这么这么不要脸**。
当所有数字都一样，`nums[mid] == nums[right]` 永远成立，每一次循环，你都分不出红蓝，只能被迫执行‘混沌右缩’，一次踢一个。
轻功高手被按进沼泽，也得一步一步爬。这叫**线性妥协**。”

“不过，”仙人话锋一转，“在绝大多数情况下，它依然能保持对数级的速度。而这一小步一小步的谨慎，正是面对不确定性时最优雅的活法。”

------

##### 尾声：模糊世界的生存法则

仙人站起身，斗篷在夜风中猎猎作响。

“记住，二分法的核心永远是**排除**。只要能安全地排除错误答案，哪怕一次只掐掉一个，你也正朝真相挪动。当逻辑失效时，**行动本身就是逻辑**。”

你看着仙人送你的这把“小刀”（right -= 1），若有所思。

原来，真正的剑法并不仅仅是关于“精准”的二分，更是关于**“不确定性”**的处理。
在 `nums[mid] == nums[right]` 的迷雾里，任何复杂的推理都是徒劳，这时候，最简单的行动——退一小步——恰恰是最有效的。

想在重复地狱里活下去，就得习惯不对称，习惯慢，习惯在混沌中一小步一小步地确认。
**因为真相这玩意儿，有时候不会轰轰烈烈地现身，而是需要一点点磨出来。**

"""
from typing import List


class Solution:
    def findMin(self, nums: List[int]) -> int:
        left, right = -1, len(nums)-1
        while left+1<right:
            mid = left + (right-left)//2
            if nums[mid]<nums[right]:
                right = mid   # 蓝染右收
            elif nums[mid]>nums[right]:
                left = mid    # 赤染左推
            else:
                right -= 1    #混沌右缩
        return nums[right]


if __name__ == '__main__':
    print(Solution().findMin([3,1,3]))
    print(Solution().findMin([1,3,3]))
    print(Solution().findMin([2, 2, 2, 0, 1, 2, 2]))
