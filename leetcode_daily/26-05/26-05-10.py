""" 2770. 达到末尾下标所需的最大跳跃次数
给你一个下标从 0 开始、由 n 个整数组成的数组 nums 和一个整数 target 。
你的初始位置在下标 0 。在一步操作中，你可以从下标 i 跳跃到任意满足下述条件的下标 j ：
0 <= i < j < n
-target <= nums[j] - nums[i] <= target
返回到达下标 n - 1 处所需的 最大跳跃次数 。
如果无法到达下标 n - 1 ，返回 -1 。

------- [翻译] ----------------------------------------------------------------------------------------------------
你是一只被施了魔法的青蛙王子。巫师的魔咒要求你必须从池塘的第 0 片荷叶跳到第 n-1 片荷叶上，那里站着一位公主——只要吻到公主，你就能变回人形。
但是，池塘里的每片荷叶上都有不同的魔法高度（整数）。你的每次跳跃，必须满足高度差不超过 target，否则你会掉进水里淹死。
同时，你也很贪心，想在变回人形之前尽可能多地踩荷叶（即最大跳跃次数），因为可以多欣赏一会儿池塘风光。
如果根本跳不到公主那里，那就只能永远当青蛙，返回 -1。

-------------------------------------------------
聪明的你拿出一张荷叶地图，上面记着每片荷叶的海拔。开始分析：
1. 先看能不能到终点
    如果所有荷叶之间根本不存在一条合法的路径（比如高度差全都不满足），那只能认栽，直接返回 -1。

2. 贪心的“慢跳”并不简单
    是不是每次跳最近的合法荷叶就能次数最多？稍加思索，你否定了这个决策，因为万一这样跳着跳着，后面突然断了呢？
    比如跳到一片荷叶后，发现后面所有的荷叶高度差得太远，你就会被困住了，而实际上从更远的地方跳过来反而能继续前进。
    所以你知道，你需要全局规划，而不是目光短浅。

3. 忍者蛙-“影分身”木牌阵
    你灵机一动，想起来师傅给你传授的独门绝技-影分身，正好适合贪心的探索前进路线。
    于是，你：
    先结印——嘭！分出无数影分身，并在每片荷叶上留一个分身拿着木牌，木牌上写着 到达这片荷叶时最多已经跳了多少次。
    一开始，只有第 0 片荷叶的木牌写着 0，其余都是 -1（表示还没分身抵达过）。
    然后你的本体从第 0 片荷叶出发，尝试跳向后面所有可能的荷叶（只要高度差 ≤ target）。
    每当你从一片荷叶 i 起跳，就分出影分身飞向后面每个合法的荷叶 j。分身到达后大喊：“我从 i 来，已经跳了 dp[i]+1 次！如果你的木牌数字小于它，就给我改掉！”
    留在荷叶 j 上的分身立刻更新木牌，始终记录 目前为止能到达它的最大跳跃次数。
    分身会消失，但木牌上的数字会一直保留，供后来的分身参考。

当你（本体和所有分身）处理完所有荷叶后，检查第 n-1 片荷叶的木牌：
如果还是 -1，说明没有任何分身到达过终点，无路可走，悲伤蛙，返回 -1。
否则，那个数字就是你能达到的 最大跳跃次数。

"""
from typing import List


class Solution:
    def maximumJumps(self, nums: List[int], target: int) -> int:
        n = len(nums)
        dp = [-1] * n   # 每个荷叶上的分身木牌（最大跳跃次数）
        dp[0] = 0       # 起点分身木牌写 0

        for i in range(n):
            if dp[i] == -1:
                continue  # 这片荷叶的木牌暗示没分身来过，跳过
            # 从 i 分出影分身，跳向所有后面合法的 j
            for j in range(i + 1, n):
                if abs(nums[j] - nums[i]) <= target:
                    dp[j] = max(dp[j], dp[i] + 1)  # 更新目标荷叶的木牌

        return dp[-1]   # 终点荷叶木牌上的数字 = 答案


if __name__ == '__main__':
    s = Solution()
    print(s.maximumJumps(nums=[1, 3, 6, 4, 1, 2], target=2))
    print(s.maximumJumps(nums=[1, 3, 6, 4, 1, 2], target=3))


