from typing import List
from math import inf


class Solution:
    def minMoves(self, nums: List[int], limit: int) -> int:
        n = len(nums)
        diff = [0] * (2*limit+3)
        for i in range(n//2):
            a = nums[i]
            b = nums[len(nums)-1-i]

            lo = min(a, b)+1
            hi = max(a, b)+limit
            s = a+b

            diff[2] += 2
            diff[2*limit+2] -= 2

            diff[lo] -= 1
            diff[hi+1] += 1

            diff[s] -= 1
            diff[s+1] += 1

        ans = float('inf')
        current_cost = 0
        for target in range(2, 2 * limit + 1):
            current_cost += diff[target]
            ans = min(ans, current_cost)
        return ans


    def minMoves_time_exceeded(self, nums: List[int], limit: int) -> int:
        # 这个 O(n × limit) 的算法在大数据下会 Time Limit Exceeded。
        ans = inf
        n = len(nums)
        for target in range(2, 2*limit+1):
            cost = 0
            for i in range(n//2):
                a = nums[i]
                b = nums[n-1-i]
                if a+b==target:
                    cost += 0
                elif (a<target and target-a<=limit) or (b<target and target-b<=limit):
                    cost += 1
                else:
                    cost += 2
            ans = min(cost, ans)
        return ans


