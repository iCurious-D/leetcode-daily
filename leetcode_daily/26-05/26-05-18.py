from typing import List
from collections import deque, defaultdict


class Solution:
    def minJumps(self, arr: List[int]) -> int:
        n = len(arr)
        if n == 1:
            return 0

        # 护法一：待办队列 (当前位置, 已走步数)
        queue = deque([(0, 0)])
        # 护法二：已踩黑名单
        seen = {0}
        # 护法三：传送门字典 值 -> 索引列表
        portal = defaultdict(list)
        for i, val in enumerate(arr):
            portal[val].append(i)

        while queue:
            i, step = queue.popleft()
            # 情形一：已到终点
            if i == n - 1:
                return step

            # 情形二：三路齐出
            # 1) 向前一步
            # 2) 向后一步
            for j in (i + 1, i - 1):
                if 0 <= j < n and j not in seen:
                    seen.add(j)
                    queue.append((j, step + 1))
            # 3) 同值传送门，用完立刻销毁
            if arr[i] in portal:
                for j in portal[arr[i]]:
                    if j not in seen:
                        seen.add(j)
                        queue.append((j, step + 1))
                # 关键优化：清空该值的传送网络，后续不再重复处理
                portal[arr[i]] = []

        return -1


